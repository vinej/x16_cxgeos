# =====================================================================
# the renderer
# =====================================================================
def _subst(d, text, params):
    """Rewrite each whole-word parameter occurrence to the dialect's reference
    form: bare (ca65/kick), prefixed (\\p 64tass, .p acme, :p mads), or
    positional ({1} dasm, \\1 vasm)."""
    pr = d["paramref"]
    for i, p in enumerate(params):
        ref = pr(p, i + 1)
        if ref == p:
            continue
        # a function replacement so a '\\' in the ref is not read as a regex escape
        text = re.sub(r"\b" + re.escape(p) + r"\b", lambda _m, r=ref: r, text)
    return text


def _op_lines(d, op, target, arg, params):
    a = _subst(d, arg, params)
    P = "X16_" + target if target else None
    if op == "pw":
        return [d["invoke"](d["pwname"], [P, a])]
    if op == "pb":
        return [INDENT + "lda #(%s)" % a, INDENT + "sta " + P]
    if op == "pl":
        return [INDENT + "lda #<(%s)" % a, INDENT + "sta " + P]
    if op == "ph":
        return [INDENT + "lda #>(%s)" % a, INDENT + "sta " + P]
    if op == "pk":
        return [INDENT + d["bank_imm"](a), INDENT + "sta " + P]
    if op == "a":
        return [INDENT + "lda #(%s)" % a]
    if op == "al":
        return [INDENT + "lda #<(%s)" % a]
    if op == "x":
        return [INDENT + "ldx #(%s)" % a]
    if op == "xh":
        return [INDENT + "ldx #>(%s)" % a]
    if op == "y":
        return [INDENT + "ldy #(%s)" % a]
    if op == "yh":
        return [INDENT + "ldy #>(%s)" % a]
    raise ValueError("unknown op %r" % op)


def _render_call(d, spec):
    name, params, comment = spec[0], spec[1], spec[2]
    ops, tail = spec[3], spec[4]
    call = spec[5] if len(spec) > 5 else "cx_" + name
    head = d["mac_open"]("cxm_" + name, params)
    if comment:
        head += "                " + d["com"] + " " + comment
    L = [head]
    for op, target, arg in ops:
        L.extend(_op_lines(d, op, target, arg, params))
    L.append(INDENT + "%s %s" % (tail, call))
    L.append(d["mac_end"])
    return "\n".join(L)


def _render_alias(d, spec):
    name, params, body, comment = spec
    head = d["mac_open"]("cxm_" + name, params)
    if comment:
        head += "                " + d["com"] + " " + comment
    inner = d["invoke"](body, [_subst(d, p, params) for p in params])
    return "\n".join([head, inner, d["mac_end"]])


def _render_builder(d, spec):
    name, params, comment, lines = spec
    head = d["mac_open"]("cxm_" + name, params)
    if comment:
        head += "                " + d["com"] + " " + comment
    go, gc = d.get("grp_open", "("), d.get("grp_close", ")")

    def grp(s):
        return s.replace("(", go).replace(")", gc) if go != "(" else s

    L = [head]
    for directive, values in lines:
        vals = ", ".join(grp(_subst(d, v, params)) for v in values)
        if directive == "raw":
            L.append(INDENT + d["byte"] + " " + vals)
        else:
            L.append(INDENT + d[directive] + " " + vals)
    L.append(d["mac_end"])
    return "\n".join(L)


def gen(dialect, version, slots):
    """Return the friendly asmsdk/<dialect>/cxrf.inc text."""
    d = DIALECTS[dialect]
    c = d["com"]
    L = [d["header"] + " -- GENERATED by abi/gen_bindings.py. Do not edit."]
    L.append(c + " =====================================================================")
    L.append(c + " CXRF asmsdk (%s) -- friendly cxm_* macros over the generated ABI" % dialect)
    L.append(c + " =====================================================================")
    L.append(c + " The low-level include (sdk/include_%s/cxrf.inc) gives the slot" % dialect)
    L.append(c + " ADDRESSES; this layer adds the named one-line cxm_* macros, the shared")
    L.append(c + " CX_* constants, and the descriptor builders -- so an app reads by intent.")
    L.append(c + " Include AFTER x16.asm (the macros use X16_P0..P7).")
    L.append(c + " =====================================================================")
    L.append("")
    if d["guard_open"]:
        L.append(d["guard_open"])
    L.append(c + " the slot addresses (cx_version = $8010, ...)")
    L.append(d["include"]("sdk/include_%s/cxrf.inc" % dialect))
    L.append("")

    # constants
    L.append(c + " =====================================================================")
    L.append(c + " constants (mirror csdk/cxsdk.h)")
    L.append(c + " =====================================================================")
    for group, items in CONSTS:
        L.append("")
        L.append(c + " --- " + group + " ---")
        for name, val, cm in items:
            line = d["equ"](name, val)
            if cm:
                line += "        " + c + " " + cm
            L.append(line)
    L.append("")
    if d["func"]:
        for name, ps, body, cm in FUNCS:
            L.append(c + " " + cm)
            L.append(d["func"](name, ps, body))
    else:
        L.append(c + " " + d["func_note"] + ":")
        for name, ps, body, cm in FUNCS:
            L.append(c + "   %s(%s) = %s" % (name, ", ".join(ps), body))
    L.append("")

    # the store helper
    L.append(c + " =====================================================================")
    L.append(c + " internal: _cx_pw stores a 16-bit value into a P-block word")
    L.append(c + " =====================================================================")
    L.append(d["cxpw"].rstrip("\n"))
    L.append("")

    # call macros
    L.append(c + " =====================================================================")
    L.append(c + " call macros -- one per ABI slot")
    L.append(c + " =====================================================================")
    for spec in CALLS:
        L.append(_render_call(d, spec))
    for spec in ALIASES:
        L.append(_render_alias(d, spec))
    L.append("")

    # descriptor builders
    L.append(c + " =====================================================================")
    L.append(c + " descriptor builders -- the byte layouts of docs/formats.md")
    L.append(c + " =====================================================================")
    for spec in BUILDERS:
        L.append(_render_builder(d, spec))

    if d["guard_close"]:
        L.append("")
        L.append(d["guard_close"])
    return "\n".join(L) + "\n"


