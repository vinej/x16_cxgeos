#!/usr/bin/env python3
"""asmsdk.py -- generate the friendly cxm_* macro layer, per assembler.

The low-level bindings (sdk/include_<dialect>/cxrf.inc) are pure address
equates and are generated by gen_bindings.py. This module is the SECOND,
friendlier layer: the cxm_* one-line macros and the descriptor builders that
today live hand-written in asmsdk/ca65/cxrf.inc.

There is ONE dialect-neutral spec here (CONSTS, FUNCS, CALLS, BUILDERS) and a
small per-dialect syntax table (DIALECTS). gen(dialect) renders the spec through
that dialect's syntax, so a new assembler is a syntax-table row, not a hand port.

Fidelity gate: the spec's ca65 rendering expands to the SAME machine code as the
committed hand-written asmsdk/ca65/cxrf.inc -- proven by assembling the app
suite with each and diffing the binaries (build.ps1 -Test). "Byte-identical"
means the expansion, exactly as the project has always meant it.

An op in a CALL's op-list is (op, target, arg):
    pw  Pn  v   -> a 16-bit store into the P-block word at X16_Pn  (_cx_pw)
    pb  Pn  v   -> lda #(v)  / sta X16_Pn        (a byte into a P-block byte)
    pl  Pn  v   -> lda #<(v) / sta X16_Pn        (pointer low  into a P byte)
    ph  Pn  v   -> lda #>(v) / sta X16_Pn        (pointer high into a P byte)
    pk  Pn  v   -> lda #^(v) / sta X16_Pn        (pointer bank into a P byte)
    a   -   v   -> lda #(v)          x  - v -> ldx #(v)     y  - v -> ldy #(v)
    al  -   v   -> lda #<(v)         xh - v -> ldx #>(v)    yh - v -> ldy #>(v)
`tail` is 'jsr' or 'jmp'; the slot label is cx_<name> unless `call` overrides it.
"""
import re

INDENT = "    "

# =====================================================================
# per-dialect syntax
# =====================================================================
# A dialect is a dict of the handful of things assemblers disagree on. The
# renderer never hard-codes ca65/64tass anywhere else.
DIALECTS = {
    "ca65": {
        "header":   "; ca65",
        "com":      ";",
        "mac_open": lambda name, ps: ".macro %s%s" % (name, (" " + ", ".join(ps)) if ps else ""),
        "mac_end":  ".endmacro",
        "invoke":   lambda name, args: "%s%s %s" % (INDENT, name, ", ".join(args)),
        "paramref": lambda n, i: n,     # a macro parameter is used bare
        "pwname":   "_cx_pw",           # the 16-bit-store helper's name
        "func_note": None,
        "byte":     ".byte",
        "word":     ".word",
        "addr":     ".addr",
        "bank_imm": lambda v: "lda #^(%s)" % v,     # ca65 has the ^ bank operator
        # the 16-bit store helper, emitted once; the stz-for-zero-high-byte
        # form the hand file uses, so the ca65 expansion stays byte-identical
        "cxpw": (
            ".macro _cx_pw addr, v\n"
            "    lda #<(v)\n"
            "    sta addr\n"
            "    .if .const(v)\n"
            "        .if (>(v)) = 0\n"
            "            stz addr+1\n"
            "        .else\n"
            "            lda #>(v)\n"
            "            sta addr+1\n"
            "        .endif\n"
            "    .else\n"
            "        lda #>(v)\n"
            "        sta addr+1\n"
            "    .endif\n"
            ".endmacro\n"
        ),
        "guard_open":  ".ifndef CXRF_ASMSDK\nCXRF_ASMSDK = 1\n",
        "guard_close": ".endif ; CXRF_ASMSDK",
        "include":  lambda p: '.include "%s"' % p,
        "func":     lambda name, ps, body: ".define %s(%s)   %s" % (name, ", ".join(ps), body),
        "equ":      lambda name, val: "%s = %s" % (name, val),
    },
    "64tass": {
        "header":   "; 64tass",
        "com":      ";",
        "mac_open": lambda name, ps: "%s .macro%s" % (name, (" " + ", ".join(ps)) if ps else ""),
        "mac_end":  ".endm",
        "invoke":   lambda name, args: "%s#%s %s" % (INDENT, name, ", ".join(args)),
        "paramref": lambda n, i: "\\" + n,   # a macro parameter is \name
        # 64tass treats a leading '_' as a LOCAL label, so the helper cannot be
        # named _cx_pw (it would be invisible where a cxm_ macro invokes it)
        "pwname":   "cxpw16",
        "func_note": None,
        "byte":     ".byte",
        "word":     ".word",
        "addr":     ".word",            # 64tass has no .addr
        "bank_imm": lambda v: "lda #((%s >> 16) & $FF)" % v,   # no ^ operator
        "cxpw": (
            "cxpw16 .macro addr, v\n"
            "    lda #<(\\v)\n"
            "    sta \\addr\n"
            "    lda #>(\\v)\n"
            "    sta \\addr+1\n"
            ".endm\n"
        ),
        "guard_open":  "",              # include-once is the caller's job in 64tass
        "guard_close": "",
        "include":  lambda p: '.include "%s"' % p,
        "func":     lambda name, ps, body: "%s .function %s\n    .endf %s" % (name, ", ".join(ps), body),
        "equ":      lambda name, val: "%s = %s" % (name, val),
    },
    # -----------------------------------------------------------------
    # ACME: !macro name .p { } ; invoke +name; params .p; data !byte/!word
    # -----------------------------------------------------------------
    "acme": {
        "header":   ";ACME",
        "com":      ";",
        "mac_open": lambda name, ps: "!macro %s%s {" % (name, (" " + ", ".join("." + p for p in ps)) if ps else ""),
        "mac_end":  "}",
        "invoke":   lambda name, args: "%s+%s %s" % (INDENT, name, ", ".join(args)),
        "paramref": lambda n, i: "." + n,
        "pwname":   "cxpw16",
        "byte":     "!byte",
        "word":     "!word",
        "addr":     "!word",            # ACME has no !addr
        "bank_imm": lambda v: "lda #((%s >> 16) & $FF)" % v,
        "cxpw": (
            "!macro cxpw16 .addr, .v {\n"
            "    lda #<(.v)\n"
            "    sta .addr\n"
            "    lda #>(.v)\n"
            "    sta .addr+1\n"
            "}\n"
        ),
        "guard_open":  "",
        "guard_close": "",
        "include":  lambda p: '!source "%s"' % p,
        "func":     None,               # ACME has no value-returning macro
        "func_note": "no value-macro in ACME; inline the expression",
        "equ":      lambda name, val: "%s = %s" % (name, val),
    },
    # -----------------------------------------------------------------
    # dasm: MAC name / ENDM ; positional {1}.. ; invoke name; data dc.b/dc.w
    # -----------------------------------------------------------------
    "dasm": {
        "header":   "; dasm",
        "com":      ";",
        "mac_open": lambda name, ps: "    MAC %s" % name,   # params are positional
        "mac_end":  "    ENDM",
        "invoke":   lambda name, args: "%s%s %s" % (INDENT, name, ", ".join(args)),
        "paramref": lambda n, i: "{%d}" % i,
        "pwname":   "cxpw16",
        "byte":     "dc.b",
        "word":     "dc.w",
        "addr":     "dc.w",
        "bank_imm": lambda v: "lda #((%s >> 16) & $FF)" % v,
        "cxpw": (
            "    MAC cxpw16\n"
            "    lda #<({2})\n"
            "    sta {1}\n"
            "    lda #>({2})\n"
            "    sta {1}+1\n"
            "    ENDM\n"
        ),
        "guard_open":  "",
        "guard_close": "",
        "include":  lambda p: '    include "%s"' % p,
        "func":     None,
        "func_note": "no value-macro in dasm; inline the expression",
        "equ":      lambda name, val: "%s = %s" % (name, val),
    },
    # -----------------------------------------------------------------
    # MADS: .macro name p / .endm ; params :p ; invoke name; data .byte/.word
    # -----------------------------------------------------------------
    "mads": {
        "header":   "; MADS",
        "com":      ";",
        "mac_open": lambda name, ps: ".macro %s%s" % (name, (" " + " ".join(ps)) if ps else ""),
        "mac_end":  ".endm",
        "invoke":   lambda name, args: "%s%s %s" % (INDENT, name, ", ".join(args)),
        "paramref": lambda n, i: ":" + n,
        "pwname":   "cxpw16",
        "byte":     ".byte",
        "word":     ".word",
        "addr":     ".word",
        "bank_imm": lambda v: "lda #((%s >> 16) & $FF)" % v,
        "cxpw": (
            ".macro cxpw16 addr v\n"
            "    lda #<(:v)\n"
            "    sta :addr\n"
            "    lda #>(:v)\n"
            "    sta :addr+1\n"
            ".endm\n"
        ),
        "guard_open":  "",
        "guard_close": "",
        "include":  lambda p: '    icl "%s"' % p,
        "func":     None,
        "func_note": "no value-macro in MADS; inline the expression",
        # MADS parses a leading '(' in a data operand as indirect addressing,
        # so grouping in a .byte/.word expression is [ ] (only the builders
        # have such expressions; the immediates keep their () fine)
        "grp_open":  "[",
        "grp_close": "]",
        "equ":      lambda name, val: "%s = %s" % (name, val),
    },
    # -----------------------------------------------------------------
    # vasm oldstyle: macro name / endm ; positional \1.. ; data byte/word
    # -----------------------------------------------------------------
    "vasm": {
        "header":   "; vasm oldstyle",
        "com":      ";",
        "mac_open": lambda name, ps: "    macro %s" % name,   # params are positional
        "mac_end":  "    endm",
        "invoke":   lambda name, args: "%s%s %s" % (INDENT, name, ", ".join(args)),
        "paramref": lambda n, i: "\\%d" % i,
        "pwname":   "cxpw16",
        "byte":     "byte",
        "word":     "word",
        "addr":     "word",
        "bank_imm": lambda v: "lda #((%s >> 16) & $FF)" % v,
        "cxpw": (
            "    macro cxpw16\n"
            "    lda #<(\\2)\n"
            "    sta \\1\n"
            "    lda #>(\\2)\n"
            "    sta \\1+1\n"
            "    endm\n"
        ),
        "guard_open":  "",
        "guard_close": "",
        "include":  lambda p: '    include "%s"' % p,
        "func":     None,
        "func_note": "no value-macro in vasm; inline the expression",
        "equ":      lambda name, val: "%s = %s" % (name, val),
    },
    # -----------------------------------------------------------------
    # KickAssembler: .macro name(p) { } ; invoke name(args); // comments;
    # .const / .label / .function ; #import ; data .byte/.word
    # -----------------------------------------------------------------
    "kick": {
        "header":   "// KickAssembler",
        "com":      "//",
        "mac_open": lambda name, ps: ".macro %s(%s) {" % (name, ", ".join(ps)),
        "mac_end":  "}",
        "invoke":   lambda name, args: "%s%s(%s)" % (INDENT, name, ", ".join(args)),
        "paramref": lambda n, i: n,     # bare, but calls use name(args)
        "pwname":   "cxpw16",
        "byte":     ".byte",
        "word":     ".word",
        "addr":     ".word",
        "bank_imm": lambda v: "lda #((%s >> 16) & $FF)" % v,
        "cxpw": (
            ".macro cxpw16(addr, v) {\n"
            "    lda #<(v)\n"
            "    sta addr\n"
            "    lda #>(v)\n"
            "    sta addr+1\n"
            "}\n"
        ),
        "guard_open":  "#importonce",
        "guard_close": "",
        "include":  lambda p: '#import "%s"' % p,
        "func":     lambda name, ps, body: ".function %s(%s) { .return %s }" % (name, ", ".join(ps), body),
        "func_note": None,
        "equ":      lambda name, val: ".const %s = %s" % (name, val),
    },
}

