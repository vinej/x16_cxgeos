def bits(rows):
    w = max((len(r.rstrip(".")) for r in rows if r.strip(".")), default=0)
    return w, [r.ljust(max(w, 1), ".")[:max(w, 1)] for r in rows]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("out", nargs="?", default="fonts/pxl6.bdf")
    args = ap.parse_args()
    L = ["STARTFONT 2.1",
         "COMMENT CXGEOS pxl6 -- a hand-drawn 6px UI face (5px caps, 4px",
         "COMMENT x-height, 1px descenders). Original to CXGEOS, public domain.",
         "COMMENT Generated by tools/pxl6gen.py -- edit the glyph table there.",
         "FONT -cxgeos-pxl6-medium-r-normal--6-60-72-72-p-6-iso8859-15",
         "SIZE 6 72 72", "FONTBOUNDINGBOX 6 6 0 -1",
         "STARTPROPERTIES 4", "FONT_ASCENT 5", "FONT_DESCENT 1",
         "DEFAULT_CHAR 32", 'COPYRIGHT "public domain"', "ENDPROPERTIES",
         "CHARS %d" % len(G)]
    for code in range(0x20, 0x7F):
        ch = chr(code)
        if ch not in G:
            continue
        w, rows = bits(G[ch])
        dw = w if ch != ' ' else 3
        L += ["STARTCHAR U+%04X" % code, "ENCODING %d" % code,
              "SWIDTH %d 0" % round(dw * 1000 / 6), "DWIDTH %d 0" % dw,
              "BBX %d 6 0 -1" % max(w, 1), "BITMAP"]
        for r in rows:
            v = sum((1 << (7 - x)) for x, c in enumerate(r) if c == "#")
            L.append("%02X" % v)
        L.append("ENDCHAR")
    L.append("ENDFONT")
    open(args.out, "w").write("\n".join(L) + "\n")
    print("wrote", args.out, "-", len(G), "glyphs")


if __name__ == "__main__":
    main()
